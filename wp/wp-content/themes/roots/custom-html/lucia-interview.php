    <div class="span8 interview-panel">
      <h3>Did Girls in Tech already exist at that time?</h3>
      Not in Toronto. One late night I was looking for a group, a network of other women in tech to reach out to. I found this organization in the States: Girls in Tech, but I noticed there wasn’t a local chapter in Toronto. So I reached out to them and told them I’d be interested in starting one. At the same time, my partner Neha was also interested so we connected and decided to get it going.

We were seeing the same people saying the same things in conferences and we wanted to bring different people, different women to the table and give them an opportunity to showcase their work and present their ideas through Girls in Tech. It’s been about a year and we’ve had six events so far. It’s really taken off. We have a good community and it keeps getting better.
    </div>
    <div class="span8 new-line offset3 interview-panel">
      <h3>Do you see the same women coming to Girls in Tech events?</h3>
      It varies. There’s always the core group but different people come out depending on the content. For one session we had an
      <a href="http://girlsintechtoronto.com/gitdot-events/women-in-tech-whos-enrolling-and-sticking-to-it/" target="_blank">academic panel</a>
      . There’s a high dropout rate of women in STEM [Science, Technology, Engineering and Math] and we wanted to ask why this was happening so we brought in some academics who do research in the area. That drew a different crowd. We met women with a tech background who came to Canada looking for jobs and were having trouble finding something. I really didn’t know how to help them. I find it interesting that that’s happening in Toronto. It stuck with me and I haven’t addressed that yet.
    </div>
