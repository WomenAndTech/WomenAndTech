        <p>We aim for 50/50 both in terms of the lead instructors and the mentors (who are like assistant instructors). We made that specific decision, because what we're looking for in the real world is not for women to be 100% of the tech industry, we just want balance. Having that reflected in our own learning environment is really important.</p>
