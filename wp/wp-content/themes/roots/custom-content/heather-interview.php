        <h3>Let's talk a little bit about the ‘women in tech’ issue. Who do you consider a woman in tech? What does a woman in tech do?</h3>
        <p>I wouldn't go around calling myself a ‘woman in tech,’ I guess because I don't want to offend the people who have a narrower definition of the term. Some say you're a woman in tech if you're in a core tech role, if you're writing code as your job. A lot of those women are mentors and volunteers for Ladies Learning Code. We do need them to call themselves women in tech and stand up as role models, and I’m glad so many of them are willing to do that.</p>
