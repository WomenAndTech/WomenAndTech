        <h3>What is your interest in youth? Or is it something coincidental because Mozilla asked you to do this?</h3>
        <p>Girls Learning Code is definitely a Ladies Learning Code initiative. We want to make a tangible difference in the number of women that are in tech roles, and in order to do that we need to reach them when they’re younger, before they’ve made decisions about their futures, before they start their careers and even before they’ve entered high school, where the courses they take determine their options in university.</p>
        <p>We’ll be following the girls who’ve come out to our workshops and Girls Learning Code. Maybe we’ll see a greater percentage of them than a control group going into STEM or STEM-related fields. It’s going to be very interesting to take a look at the numbers and the feedback. At some point, maybe we’ll be able to secure a few dollars for a formal study in order to dive in and ask, “What kind of impact is this making?” That would be really cool.</p>
