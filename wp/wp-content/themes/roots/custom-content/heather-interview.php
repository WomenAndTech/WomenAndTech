        <p>We've had such amazing insight into this through running Girls Learning Code. The thing that scares me the most is that 9-year-old girls (the girls who come to our camp are 9-13) already know that the tech industry is dominated by men. A lot of them comment that they like that Girls Learning Code is girls-only because it gives them a chance to explore without feeling like they're doing something nerdy or weird.</p>
