        <p>The second thing, in the short-term, is having a cool life where I feel excited every day about my mission. The fact that <a href="http://hackeryou.com/guest-post-my-time-at-hackeryou/" target="_blank">people actually like HackerYou</a> is so exciting to me. With Ladies Learning Code, it’s already a success; now we’re pushing forward and managing risks, creating a space and expanding to new cities.</p>
