      <section class='row normal'>
        <div class="span7 offset4 interview-panel">
         <h3 id="in-our-last-interview-karen-schulman-dupuis-asked-when-were-you-most-vulnerable-and-what-was-the-best-thing-that-came-from-that-">In our last interview, Karen Schulman Dupuis asked: When were you most vulnerable, and what was the best thing that came from that?</h3>

         <p>I love this question. Vulnerability seems so central to understanding oneself. And if I had to define entrepreneurship in my own terms I&#39;d describe it as a personal odyssey to continually discover that which one has to give to the world. </p>

         <p>The story I want to tell is from the spring of my senior year of high school. Throughout high school, I was very active with sports &amp; photography but I always admired people in the drama club and wondered what it would feel like to get up on stage and be so exposed in front of so many people. Well, my last semester, I landed a lead role in a short play as a woman in a tumultuous relationship with the male lead.  </p>

         <p>At opening night, the guy playing opposite me was a no show. There were other plays lined up for the evening, and just before the drama instructor removed us from the lineup, I approached her with a proposal. I persuaded another guy to play the male lead. All we needed was a minor rewrite to embed our play within a story that starts with a casting call for a male lead. That way, we could perform the original play with the male lead holding a script in his hand. She loved it and the show went on. But part way through, the original male lead showed up. Even though I was furious with him, I let him up on the stage. Fortunately, the script called for an argument, and I delivered! We ended up improvising most of the rest. It was tons of fun and the crowd loved it – or at least that’s how I felt, who knows what they really thought!</p>

         <p>Many of my friends and schoolmates were in the audience that evening, including my high school crush. I could have taken the easy road and bailed when he didn&#39;t show. What if my idea didn&#39;t work? What if people didn&#39;t like it? What if I did something embarrassing and made a complete fool of myself? Looking back, I&#39;m not sure if any of these things even entered my mind, I just knew I worked hard at the role, there was an audience expecting a show, and I wanted to give them a performance, even if it meant being a little inventive and more vulnerable. That experience reminds me to just go for it! When you do, there&#39;s a really good chance you&#39;ll be happy you did, and may even inspire others to go after something themselves.</p>
