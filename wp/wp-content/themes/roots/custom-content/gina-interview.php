        <div class="span7 interview-panel">
          <h3 id="who-have-influenced-you-the-most-in-your-journey-so-far-">Who have influenced you the most in your journey so far?</h3>

          <p>I was really impacted by the way Bill Warner approached his startups. It’s a fresh approach that impacts the founder rather than just starting a startup. He has a specific philosophy that he calls “building a startup from the heart.” A lot of it is focusing on the startup’s intentions - it sounds so obvious but so many people don’t do it. You have got to take that step back. If you don’t have clarity as to why you personally are doing it, you are just not going to be as effective or not going to build the company you want. Bill has been extremely influential in my life since I met him.</p>
        </div>

        <div class="span7 offset4 interview-panel">

          <p>Mentorship is interesting. I have learned from a lot of people. I love the community here in Boston. There is this legacy of startup beginnings. <a href="http://www.hubspot.com/" target="_blank">HubSpot</a> hangs on to that freshness and acknowledgment for continuous invention. The range goes from that and <a href="http://www.hubspot.com/company/management/dharmesh-shah" target="_blank">Dharmesh Shah</a> to Sloan.</p>

          <p>Apart from that, <a href="http://web.media.mit.edu/~mres/" target="_blank">Mitchel Resnick</a> and the work that I do as a mentor in the <a href="http://www.computerclubhouse.org/" target="_blank">Computer Clubhouse</a> have introduced an approach of learning I go to every day of my life. I have transformed as a person thanks to Mitch and Bill.</p>
