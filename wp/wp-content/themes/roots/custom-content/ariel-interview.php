      <div class="span7 offset4 interview-panel">
        <h3>At what point did science became an important influence in your life?</h3>
        <p>As a kid, I hated computers. I thought they were anti-natural, and I was very into the natural world. Science is a part of the natural world. In high school I did great at science and I did okay at math, but I was into abstract concepts. I loved logic &mdash; loved, loved, loved logic, like I loved doing puzzles. When everyone in university hated multiple choice, I loved multiple choice. Just to know the totality of the answers!</p>
