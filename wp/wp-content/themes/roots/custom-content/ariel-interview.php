      <div class="span7 offset4 interview-panel">
        <h3>Why do you think there aren’t more young women choosing to pursue a career in science?</h3>
        <p>There’s definitely a tendency where people can, from a young age, identify, ‘I’m good at this, I’m not good at this, and this is scary.’ And if you decide you aren’t good at something, you don’t take the time to build the skill. You shut down and you don’t engage with it.</p>
