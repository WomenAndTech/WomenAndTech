        <h3>Do you think there is a difference between men and women in identifying with science?</h3>
        <p>I'm tempted to think that early on there was a misidentification about who was good at math and who wasn't. And as we break down those gender barriers, my guess would be that in classrooms now you see much less differentiation between number of males and females at a young age. There are so many girls who rock at math!</p>
        <p>In my generation, to get into computational neuroscience you had to do computer science first. I think in 15 years, in places like computational neuroscience where there are equal opportunities to learn computer science, or if it becomes acceptable for girls to sit in their basement and figure out code, we’ll see leveling off of males vs. females.</p>
      </div>
    </section>
    <section class="row">
      <div class="span7 offset4 interview-panel">
        <h3>What about your role as a female tech entrepreneur.  Can you speak to that?</h3>
        <p>Well, I'm definitely in the vast minority. For example, I was invited to a tech conference for the 50 brightest minds in technology. They flew us on a private jet to Hawaii, made it a big deal, it was pretty cool. There were 50 people – only 5 were women.</p>
        <p>As a rare bird in this full nest, I have to say I don’t mind. I can work being female to my advantage. We joke that if our demo isn’t working at a conference, I can just wear the headset and smile and that would be enough.</p>
        <p>Everyone has their own advantages regardless of gender. I’m in a unique position at this point in time as a woman tech CEO.</p>
      </div>
    </section>
    <section class="row">
      <div class="span7 offset4 interview-panel">
        <h3>Do you think there are advantages to being a woman in technology?</h3>
        <p>I'm aware that because I am a rare female (in the tech executive space), I may receive special treatment. And probably part of my success is due to the fact that I’m female. It’s fetishized, not in a sexual way, but through that fetishization the female role becomes iconic, creating an archetype other people can follow. That leads to role models and creates a path for more women.</p>
