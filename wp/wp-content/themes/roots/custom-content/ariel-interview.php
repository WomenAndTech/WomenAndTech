      <div class="span7 interview-panel">
        <h3>How do you define ‘technology’?</h3>
        <p>Technology is an enabler. Anything is a technology; the glasses we wear are technology; there is technology in my shoes. The definition we use today is very specific and it tends to refer to digital or computational technologies that enable our lives in ways that weren’t possible two and a half decades ago.</p>
      </div>

    </section>
    <section class="row">
      <div class="span7 offset4 interview-panel">
        <h3>What were your earliest interactions with technology?</h3>
        <p>I was never really into technology. It wasn’t something that I identified with. It wasn’t a driving force. But in the creation of this brainwave technology, there was a lot of processing involved that creates the core of the experience. Starting work in the early 2000s on early brain wave systems was my first real foray into technology as something that was part of my own identity.</p>
      </div>
    </section>
    <section class="row">
      <div class="span7 offset4 interview-panel">
