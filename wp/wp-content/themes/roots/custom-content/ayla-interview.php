              I’m really interested in exploring how technology and human emotion come together. How can we make what we traditionally think of as technology be a bit more human?
            </p>

            <p>
              Right now I’m writing a book, Dating by Design
              <http://datingbydesign.ca/>
              . The idea is to take the technologies of design – the processes, methodologies and tools – and apply them to relationships. I’m looking at how we can build better relationships by looking at them through a lens that’s not so emotional.
            </p>
          </div>
        </section>
        <section class="row">
          <div class="span7 offset4 interview-panel">
            <h3>What do you gain by removing emotion?</h3>
            <p>
              Perspective. For example, I did a bunch of interviews when I first started working on this project and I talked to a guy who said he had spent 15,000 hours developing skills for his career. I asked him how long he’d spent developing his skills around relationships and he said,
              <em>“Maybe 15.”</em>
              That’s amazing! We don’t spend time thinking about relationships as a skill. We’re not asking the right questions. To have better relationships we need to design them more intentionally.
            </p>
          </div>
        </section>
        <section class="row">
          <div class="span7 offset4 interview-panel">
            <h3>
              Would you consider designing relationships between people to be one of your life goals?
            </h3>
            <p>
              I think certainly helping people develop emotional intelligence is a big goal for me. Perhaps starting a school like the School of Life, that would be a major life accomplishment.
            </p>
